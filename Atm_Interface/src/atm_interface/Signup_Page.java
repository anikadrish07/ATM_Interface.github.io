    boolean Validation() {
        
        accountNo = txt_AccountNo.getText();
        cardNo = txt_CardNo.getText();
        name = txt_Name.getText();
        pin = txt_Pin.getText();
        confirmPin = txt_ConfirmPin.getText();
        contactNo = txt_PhoneNo.getText();
        dob = txt_dob.getDate();
        
        if(accountNo.equals("")){
            JOptionPane.showMessageDialog(this, "please enter account number");
            return false;
        }
        
        if(cardNo.equals("")){
            JOptionPane.showMessageDialog(this, "please enter card number");
            return false;
        }
        
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "please enter account holder name");
            return false;
        }
        
        if(pin.equals("")){
            JOptionPane.showMessageDialog(this, "please enter 4 digit pin");
            return false;
        }
        
        if(confirmPin.equals("")){
            JOptionPane.showMessageDialog(this, "enter confirm pin");
            return false;
        }
        
        if(dob.equals(null)){
            JOptionPane.showMessageDialog(this, "please enter date of birth");
            return false;
        }
        
        if(!pin.equals(confirmPin)){
            JOptionPane.showMessageDialog(this, "password not matched");
            return false;
        }   
        if(contactNo.equals("")){
            JOptionPane.showMessageDialog(this, "enter contact no");
            return false;
        }
        return true;
    }
    
