    void insertDetails(){
        SimpleDateFormat format = new SimpleDateFormat("YYYY-MM-dd");
        String myDob = format.format(dob);
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atm_interface", "root", "");
            String sql = "insert into signup values(?,?,?,?,?,?,?,?)";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, accountNo);
            stmt.setString(2, cardNo);
            stmt.setString(3, name);
            stmt.setString(4, pin);
            stmt.setString(5, confirmPin);
            stmt.setString(6, myDob);
            stmt.setString(7, contactNo);
            stmt.setInt(8, balance);

            int i = stmt.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(this, "Signup Successful");
            } else {
                JOptionPane.showMessageDialog(this, "Record not inserted");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
